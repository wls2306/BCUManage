package com.bcu.entity;

public class BrRecord {
    private String brId;
    private String brGoalId;
    private String brGoalName;
    private String brType;
    private String brInitiator;
    private String brAuditor;
    private String brTime;

    public String getBrId() {
        return brId;
    }

    public void setBrId(String brId) {
        this.brId = brId;
    }

    public String getBrGoalId() {
        return brGoalId;
    }

    public void setBrGoalId(String brGoalId) {
        this.brGoalId = brGoalId;
    }

    public String getBrGoalName() {
        return brGoalName;
    }

    public void setBrGoalName(String brGoalName) {
        this.brGoalName = brGoalName;
    }

    public String getBrType() {
        return brType;
    }

    public void setBrType(String brType) {
        this.brType = brType;
    }

    public String getBrInitiator() {
        return brInitiator;
    }

    public void setBrInitiator(String brInitiator) {
        this.brInitiator = brInitiator;
    }

    public String getBrAuditor() {
        return brAuditor;
    }

    public void setBrAuditor(String brAuditor) {
        this.brAuditor = brAuditor;
    }

    public String getBrTime() {
        return brTime;
    }

    public void setBrTime(String brTime) {
        this.brTime = brTime;
    }
}
