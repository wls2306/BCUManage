package com.bcu.entity;

public class Parts {
    private String partsId;//id
    private String partsName;//名字
    private String partsUnit;//单位
    private String partsNumber;//数量
    private String partsStock;//库存
    private String partsStatus;//现状
    private String partsManager;//管理人
    private String partsLocation;//存放地

    public String getPartsId() {
        return partsId;
    }

    public void setPartsId(String partsId) {
        this.partsId = partsId;
    }

    public String getPartsName() {
        return partsName;
    }

    public void setPartsName(String partsName) {
        this.partsName = partsName;
    }

    public String getPartsnit() {
        return partsUnit;
    }

    public void setPartsUnit(String partsnit) {
        this.partsUnit = partsnit;
    }

    public String getPartsNumber() {
        return partsNumber;
    }

    public void setPartsNumber(String partsNumber) {
        this.partsNumber = partsNumber;
    }

    public String getPartsStock() {
        return partsStock;
    }

    public void setPartsStock(String partsStock) {
        this.partsStock = partsStock;
    }

    public String getPartsStatus() {
        return partsStatus;
    }

    public void setPartsStatus(String partsStatus) {
        this.partsStatus = partsStatus;
    }

    public String getPartsManager() {
        return partsManager;
    }

    public void setPartsManager(String partsManager) {
        this.partsManager = partsManager;
    }

    public String getPartsLocation() {
        return partsLocation;
    }

    public void setPartsLocation(String partsLocation) {
        this.partsLocation = partsLocation;
    }
}
